public class EmbeddingServiceTests
{
    [Fact]
    public async Task ComputeEmbedding_ReturnsZeroVectorWhenModelAbsent()
    {
        var opts = Options.Create(new NeoOptimizeClientOptions
        {
            EnableLocalAI = true,
            LocalModelsDirectory = Path.Combine(Path.GetTempPath(), "nonexistent_models"),
        });

        var svc  = new EmbeddingService(opts, NullLogger<EmbeddingService>.Instance);
        var emb  = await svc.ComputeEmbeddingAsync("Hello world");

        // Should return a 384-dim zero vector (graceful fallback)
        Assert.Equal(384, emb.Length);
        Assert.All(emb, v => Assert.Equal(0f, v));
        Assert.False(svc.IsAvailable);
    }
}

