public class EngineServiceTests
{
    [Fact]
    public async Task ClamAvScan_ReturnsHelpfulMessageWhenNotInstalled()
    {
        var opts = Options.Create(new NeoOptimizeClientOptions { BackendBaseUrl = "http://localhost:8000" });

        // NeoEngineBridge and LocalMemoryStore are sealed 풉칂칬 pass null and rely on graceful handling
        // EngineService.RunClamAvScanAsync checks for clamscan.exe first, before using _bridge
        var svc = new EngineService(null!, null!, opts, NullLogger<EngineService>.Instance);

        // This should return a result (not throw), with a message about clamscan not found
        var result = await svc.RunClamAvScanAsync(@"C:\NonExistentTargetPath");
        Assert.NotNull(result.Message);
        Assert.False(result.Success);
        Assert.Contains("tidak ditemukan", result.Message);
    }
}
