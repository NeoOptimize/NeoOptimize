public class BloatwareServiceTests
{
    private readonly ILogger<BloatwareService> _logger = NullLogger<BloatwareService>.Instance;

    [Fact]
    public async Task GetDatabase_ReturnsAtLeast100Entries()
    {
        var svc = new BloatwareService(_logger);
        var db  = await svc.GetDatabaseAsync();
        Assert.True(db.Count >= 100, $"Expected >= 100 entries, got {db.Count}");
    }

    [Fact]
    public async Task GetDatabase_NoHighRiskEntryWithoutNote()
    {
        var svc = new BloatwareService(_logger);
        var db  = await svc.GetDatabaseAsync();
        var highRiskNoNote = db.Where(e => e.Risk == "high" && string.IsNullOrEmpty(e.Note)).ToList();
        Assert.Empty(highRiskNoNote); // Every "high" risk entry must have a warning note
    }

    [Fact]
    public async Task Remove_HighRiskBlocked()
    {
        var svc   = new BloatwareService(_logger);
        var db    = await svc.GetDatabaseAsync();
        var entry = db.First(e => e.Risk == "high");

        var result = await svc.RemoveAsync(entry);
        Assert.False(result.Success);
        Assert.Contains("High-risk", result.Message);
    }

    [Fact]
    public async Task DryRun_RemoveAllRecommended_NothingActuallyDeleted()
    {
        var svc     = new BloatwareService(_logger);
        var results = await svc.RemoveAllRecommendedAsync(dryRun: true);

        // In dry-run mode, all results should indicate "Would remove"
        foreach (var r in results)
            Assert.Contains("Dry Run", r.Message);
    }
}

